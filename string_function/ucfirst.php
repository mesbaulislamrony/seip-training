<?php
	$str_a  = 'hello world!';
	$str_b  = 'HELLO WORLD!';
	
	// Make a string's first character uppercase
	echo ucfirst($str_a);
	
	echo "<br>";
	
	// Make a string's first character lowercase
	echo lcfirst($str_b);
	
	echo "<br>";
	
	// Make a string uppercase
	echo strtoupper($str_a);
	
	echo "<br>";
	
	// Make a string lowercase
	echo strtolower($str_b);
?>